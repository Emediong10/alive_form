<h4 id="faq-11"><strong>Q.</strong> How do I pay for items on the Marketplaces?</h4>
<p class="mb-0">LChrist is the center of our education. He is the One who created all that we study, and a greater knowledge 
and understanding of Him and His works leaves us better prepared to present Him to the unbelieving. <br><br>
No education is worthy of the name if He “in Whom are hidden all the treasures of wisdom and knowledge” is 
left out (Colossians 2:3).</p>
<p>“In the Teacher sent from God, all true educational work finds its center…In the presence of such a Teacher, 
of such opportunity for divine education, what worse than folly is it to seek an education apart from Him—to 
seek to be wise apart from Wisdom; to be true while rejecting Truth; to seek illumination apart from the Light, 
and existence without the Life; to turn from the Fountain of living waters, and hew out broken cisterns, that can 
hold no water” (Education, p. 83).</p> <p>Jesus educates through the Bible: “All Scripture is given by inspiration of God, and is profitable for doctrine, 
for reproof, for correction, for instruction in righteousness, that the man of God may be complete, thoroughly 
equipped for every good work.” (2 Timothy 3:16, 17).</p> <p>Jesus educates by the Holy Spirit: “TheHelper, the Holy Spirit, whom the Father will send in My name, He will 
teach you all things” (John 14:26). “God can teach you more in one moment by His Holy Spirit than you could 
learn from the great men of the earth” (Testimonies to Ministers and Gospel Workers, p. 119).
Jesus educates through nature, useful work, and the experiences of life: “[Jesus’] education was gained 
directly from Heaven-appointed sources; from useful work, from the study of the Scriptures and of nature, and 
from the experiences of life—God’s lesson books, full of instruction to all who bring to them the willing hand, 
the seeing eye, and the understanding heart” (Education, p. 77).</p>

