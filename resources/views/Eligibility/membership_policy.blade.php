<h4 id="faq-11"> Reading</h4>
<p class="mb-0">What we put into our brains is what we become. Reading material must be selected carefully, based on the 
standards laid out in Philippians 4:8: “Finally, brethren, whatever things are true, whatever things are noble, 
whatever things are just, whatever things are pure, whatever things are lovely, whatever things are of good 
report; if there is any virtue, and if there is anything praiseworthy—meditate on these things.”</p>
<p>“The mind…is built up by that upon which it feeds; and it rests with us to determine upon what it shall be fed” 
(The Faith I Live By, p. 20). “It is a law of the human mind that by beholding we become changed. Man will 
rise no higher than his conceptions of truth, purity and holiness. If the mind is never exalted above the level of 
humanity, if it is not uplifted by faith to contemplate wisdom and love, the man will be constantly sinking lower 
and lower” (Patriarchs and Prophets, p. 91), because “the mind…gradually adapts itself to the subjects upon 
which it is trained to dwell” (Mind, Character, and Personality, vol. 2, p. 418).
</p>
<p>This applies with singular force to printed matter. “Of making many books,” declares Solomon, “there is no 
end” (Ecclesiastes 12:12). And notwithstanding the educational value in some of these endless publications, 
it is mostly a blend of ideas picked from the tree of good and evil. And the Lord admonishes us not to “[create] 
a distaste for life’s practical duties” (Counsels to Parents, Teachers, and Students, p. 383) by the reading of 
that which is fictitious, romantic, or nonessential</p>

<div class="line"></div>

<h4 id="faq-11"><strong>Q.</strong>  Electronic Media</h4>
<p class="mb-0">Radio and television, like books, are valuable resources, but we must carefully select the things we listen to 
and watch.
</p>
<p>Nothing educates the masses faster or more thoroughly than the medium of radio and television. Unfortunately, 
most of the programming is not supplied with heaven in mind. So “if we are not discriminating, [radio and 
television] will bring sordid programs right into our homes.”</p><p>“Safety for ourselves and our children is found in a determination, by God’s help, to follow the admonition of 
the apostle Paul: ‘Finally…whatever things are true…noble, just, pure, lovely, of good report, if there is any 
virtue and if there is anything praiseworthy–meditate on these things” (Seventh-day Adventist Church Manual, 
p. 148).</p> <p>Recreation and Entertainment <p>
Recreation is activity designed to refresh and recharge the powers of body and mind. This we do with the glory 
of God in mind, engaging in activities that are uplifting and encourage harmony. <p>
“Whatever you do, do all to the glory of God” (1 Corinthians 10:31). </p><p>
“Many of the amusements popular in the world today, even with those who claim to be Christians, tend to the 
same end as did those of the heathen. There are indeed few among them that Satan does not turn to account 
in destroying souls…. In every gathering for pleasure where pride is fostered or appetite indulged, where one 
is led to forget God and lose sight of eternal interests, there Satan is binding his chains about the soul.” 
(Patriarchs and Prophets, p. 459).
It must also be recognized that any activity which fosters rivalry rather than loving cooperation is an offense to 
God, for its tendency is to strain the tender relationships we enjoy one with another.</p>

<div class="line"></div>

<h4 id="faq-11"> Music</h4>
<p class="mb-0">Music is one of the most debated topics. Meant for our enjoyment and enhancement, it has been debased to 
the point where we must exercise the utmost care as we select the songs we listen to and perform. We seek 
out music that leads us closer to God without taking control of our emotions.
</p> <p>“Music was made to serve a holy purpose, to lift the thoughts to that which is pure, noble, and elevating, and 
to awaken in the soul devotion and gratitude to God” (Patriarchs and Prophets, p. 594). Jesus “held communion 
with heaven in song.” (The Desire of Ages, p. 73)</p> <p>“Music is one of the highest arts. Good music not only gives pleasure, but elevates the mind and cultivates the 
finest qualities. God often has used spiritual songs to touch the hearts of sinners and lead to repentance” 
(Seventh-day Adventist Church Manual, p. 149). But like everything else, music has been debased for the 
purpose of destroying the fine rhythms of the soul and for the breaking down of morality.</p>

<div class="line"></div>

<h4 id="faq-11"> Social Relationships</h4>
<p class="mb-0">Friendship and fellowship are important to our whole wellbeing. We enjoy interacting, keeping in mind that 
each communication is an opportunity for growth and for giving glory to God. <br><br>
“God gives us the social instinct for our pleasure and benefit. ‘by mutual contact minds receive polish and 
refinement; by social intercourse, acquaintances are formed and friendships contracted which result in a unity 
of heart and an atmosphere of love which is pleasing in the sight of heaven.’…Such associations should be 
conducted upon a high plane and with due regard for the social conventions that have been prescribed for our 
protection. It is the purpose of Satan to pervert every good thing, and the perversion of the best often leads to 
that which is worst” (Testimonies for the Church, vol. 6, p. 172) (---------------Seventh-day Adventist Church 
Manual, p. 151).
</p>

<div class="line"></div>

<h4 id="faq-11"> Courtship and Marriage</h4>
<p class="mb-0">Within marriage we can experience a taste of God’s relationship to us. However, as we seek a partner for life, 
we must not be guided by misleading emotions. Entering into this relationship is a process to be enjoyed, but 
it is also a process requiring much prayer, guidance, and the highest level of self-discipline.
</p> <p>Because the natural attraction between men and women is so strong, it often plays into the hands of the 
enemy. History is replete with the stories of men and women, of otherwise faultless deportment, who have 
been overthrown by some subtle, seductive appeal from the opposite gender. ”Therefore let him who thinks 
he stands take heed lest he fall” (1 Corinthians 10:12).</p> <p>“Satan is busily engaged in influencing those who are wholly unsuited to each other to unite their interests. He 
exults in this work, for by it he can produce more misery and hopeless woe to the human family than by 
exercising his skill in any other direction” (Messages to Young People, p. 455).</p><p>As a matter of fact, “the majority of the marriages of our time, and the way in which they are conducted, make 
them one of the signs of the last days. Men and women are so persistent, so headstrong, that God is left out 
of the question. Religion is laid aside, as if it had no part to act in this solemn and important matter” (The 
Review and Herald, September 25, 1888).</p> <p>“Early marriages are not to be encouraged. A relation so important as marriage and so far-reaching in its 
results should not be entered upon hastily, without sufficient preparation, and before the mental and physical 
powers are well developed” (Messages to Young People, p. 438).</p> <p>Seek a Believer: “Do not be unequally yoked together with unbelievers” (2 Corinthians 6:14). “However pure 
and correct one’s principles may be, the influence of an unbelieving companion will have a tendency to lead 
away from God” (Patriarchs and Prophets, p. 174). <br><br>
Weigh and Watch: “Let those who are contemplating marriage weigh every sentiment and watch every 
development of character in the one with whom they think to unite their life destiny” (The Ministry of Healing, 
p. 359).</p> <p>Pray: “If any of you lacks wisdom, let him ask of God, who gives to all liberally and without reproach, and it will 
be given to him” (James 1:5). “If men and women are in the habit of praying twice a day before they contemplate 
marriage, they should pray four times a day when such a step is anticipated” (The Adventist Home, p. 71).
Counsel: “In the multitude of counselors there is safety” (Proverbs 11:14). “The reason that so grave mistakes 
are made by the youth is that they do not learn from the experience of those who have lived longer than they 
have” (Counsels to Parents, Teachers, and Students, p. 223). “If you are blessed with God-fearing parents, 
seek counsel of them. Open to them your hopes and plans, learn the lessons which their life experiences have 
taught, and you will be saved many a heartache” (Messages to Young People, p. 435).
Marriage “was designed to be a blessing to mankind. And it is a blessing wherever the marriage covenant is 
entered into intelligently, in the fear of God, and with due consideration for its responsibilities” (The Adventist 
Home, p. 18).</p>

